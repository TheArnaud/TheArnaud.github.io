        <h3 style="text-align: center"><b>Les Enseignements en Terminale<br></b></h3> <br>
        6 matières sont proposées. Les élèves peuvent en choisir 3 ou 4.
        <ol style="padding-left:0">
            <br>


            <li class="compt">
                <p class="titleol"><b>
                        Culture Générale et Philosophie
                    </b></p>
                <br><br>La Culture Générale en tant que telle n’est pas une matière du Baccalauréat. Elle est pourtant
                indispensable aux élèves qui souhaitent « faire la différence » et aussi se préparer à l’Enseignement
                Supérieur de haut niveau. Les connaissances acquises seront un réel atout en Terminale mais aussi bien
                au-delà.

                <br><br>Les cours de Culture Générale couvriront des domaines vastes et avec pour dénominateurs communs
                :
                </p>


                <ul>
                    <li>
                        L’acquisition de connaissances solides et différenciantes dans les principaux faits de société
                    </li>

                    <li>
                        La compréhension des sujets qui font aujourd’hui débat
                    </li>

                    <li>
                        La capacité à réfléchir, à problématiser et apporter un éclairage personnel structuré et
                        construit
                    </li>
                </ul>
            </li>
            <br>

            <li class="compt">
                <p class="titleol">
                    <b>Maîtrise de l'Oral</b>
                </p>
                <ul><br>
                    <li> Techniques de prise de parole</li>
                    <li> Convaincre lors de son oral </li>
                    <li> Capter l’attention de son auditoire</li>
                    <li> Richesse lexicale</li>
                    <li> Aisance oratoire</li>
                    <li> Gestion du stress face à un jury</li>
                </ul>
                <br>
            </li>

            <li class="compt">
                <p class="titleol">
                    <b>Préparation du Dossier et des Ecrits Personnels Sciences Po Paris</b>
                </p><br><br>
                Pour les élèves qui candidatent à Sciences Po Paris, les ecrits personnels jouent un rôle déterminant dans la procédure d’admission. Ces cours vous donneront la méthode et les bases pour réussir ces exercices.
                <br>
            </li>
                <br>
            <li class="compt">
                <p class="titleol">
                    <b>Anglais</b>
                </p>
                <br><br>Outre le suivi des attentes et des exigences des épreuves du baccalauréat, les cours d’Anglais
                se veulent être une vraie anticipation de l’Enseignement Supérieur :
                </p>


                <ul>
                    <li>
                        Connaissances des enjeux civilisationnels des pays anglo-saxons
                    </li>

                    <li>
                        Enrichissement lexical et excellente maîtrise de l’Anglais écrit
                    </li>

                    <li>
                        Fluidité de l’Anglais oral et travail sur l’aisance
                    </li>

                    <li>
                        Objectif attendu en fin d’année : C1
                    </li>
                </ul>
            </li>
            <br>
            

            <li class="compt">
                <p class="titleol">
                    <b>Histoire et Géopolitique</b>
                </p>
            </li>

            <li class="compt">
                <p class="titleol">
                    <b>Sciences Economiques et Sociales</b>
                </p>


                <br><br>Dans les spécialités : <b>Histoire Géopolitique et Sciences Politiques</b> ainsi que
                    <b>Sciences Economiques et Sociales</b>, le programme suit celui du baccaularéat avec un accent particulier mis sur les aspects suivants :
                </p>

                <p>
                <ul>
                    <li>
                        Parfaite maîtrise et assimilation des points du Programme
                    </li>
                    <br>
                    <li>
                        Travail sur la Méthodologie et la capacité des élèves à optimiser les connaissances
                    </li>
                    <br>
                    <li>
                        Travail sur le raisonnement, l’argumentation et la capacité de réflexion
                    </li>
                    <br>
                    <li>
                        Acquisition de connaissances « supérieures » à celles attendues d’un élève « moyen » au
                        baccalauréat de sorte à ce que les élèves qui suivent ces enseignements aient un réel avantage
                        comparatif par rapport aux lycéens de Terminale traditionnels
                    </li>
                </ul>
                
            </p>

        
