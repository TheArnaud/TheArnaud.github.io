        <p>
            Les nouvelles modalités du Baccalauréat et l’importance des dossiers scolaires dans les choix d’orientation
            de l’Enseignement Supérieur nécessitent des dossiers scolaires très solides.
            <br><br> Cela vaut tant pour les bulletins scolaires du lycée et le contrôle continu que pour les résultats
            aux épreuves du baccalauréat.
            <br><br>
            Dans ce contexte, nous proposons un accompagnement des élèves de Terminales tout au long de l’année dans les
            matières de spécialités. Dans chaque matière, l’objectif est double :

        </p>
        <ol style="padding-left:0">
            <li class="compt">
                <p class="titleol">
                    <b>Muscler les résultats scolaires</b> et le niveau académique
                </p>

            </li>
            <li class="compt">
                <p class="titleol">
                    Donner une méthode de travail permettant à l’élève d’adopter dès la classe de Terminale <b>les
                        méthodes de l’Enseignement Supérieur de haut niveau</b></p>


        </ol>

        <p>
            <b>Les cours sont dispensés par des professeurs de Grandes Ecoles et/ou de Classes Préparatoires.
            </b>
        </p>
        
        <hr style=" margin-top:30px;margin-bottom:30px;">
        <p><b>Les cours en Terminale doivent :</b></p>

        <ol style="padding-left:0">
            <li class="compt">
                <p class="titleol">Permettre aux élèves de considérablement solidifier leur niveau scolaire au Lycée en
                    visant une amélioration de leurs notes de 2 à 4 points en moyenne. </p>

            </li>
            <li class="compt">
                <p class="titleol">Leur donner des Méthodes de Travail pour gagner en efficacité. </p>

            </li>
            <li class="compt">
                <p class="titleol">Anticiper leur entrée dans l’Enseignement Supérieur en leur donnant les bases
                    nécessaires et les pré-requis à acquérir dès le Lycée pour s’ouvrir toutes les portes, et tout
                    spécifiquement celles des filières les plus exigeantes dans le post-bac. </p>

            </li>
        </ol>
