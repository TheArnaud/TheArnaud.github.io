		Ils ont plusieurs objectifs :</h3>
		<ol style="padding-left:0">
			<li class="compt">
				<p class="titleol">Permettre aux élèves de considérablement solidifier leur niveau scolaire au Lycée en visant une amélioration de leurs notes de 2 à 4 points en moyenne. </p>

</li>
<li class="compt">
				<p class="titleol">Leur donner des Méthodes de Travail pour gagner en efficacité. </p>

</li>

<li class="compt">
				<p class="titleol">Anticiper leur entrée dans l’Enseignement Supérieur en leur donnant les bases nécessaires et les pré-requis à acquérir dès le Lycée pour s’ouvrir toutes les portes, et tout spécifiquement celles des filières les plus exigeantes dans le post-bac. </p>

</li>

</ol>


		<b> La préparation annuelle en classe de Première commence le 11 novembre 2022 pour se terminer en mai 2023.</b> 
		
		
			<br><br>
			<ul>
			<li>
		60 heures réparties sur la durée de la préparation et planifiées de sorte à permettre de concilier les cours dispensés dans le cadre de la préparation et les enseignements au lycée.</li>
				
    			<br><li>
				Trois jours de cours partiels en début d’année de préparation : 11 au 13 novembre 2022.
			</li>
    			<br><li>
				Des cours répartis entre novembre et mai à raison d’une ½ journée toutes les deux ou trois semaines selon le calendrier.
			</li>
        
		<br><li>
				Les cours auront lieu soit le <b>samedi matin</b> soit le <b>samedi après-midi</b> selon les groupes d’affectation.
			</li>
            <br><li>
				Il n’y a pas de cours pendant les vacances scolaires hormis un ou deux jours pendant les vacances de printemps.
			</li><br>
            </ul>
