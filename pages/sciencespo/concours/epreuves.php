		<h3>La phase d'admission consiste en un entretien de motivation</h3>
		<ul>
			Les candidats déclarés admissibles passeront un entretien devant une commission composée de deux membres au moins, dont un représentant du directeur de Sciences Po et un enseignant de Sciences Po. Ils disposent de l'intégralité du dossier du candidat.
				<br />
    				D'une durée de vingt minutes, l'entretien d'admission permettra d'évaluer, selon une grille de critères préalablement définis, la motivation du candidat, son ouverture d'esprit, son goût pour l'innovation, sa curiosité intellectuelle, sa capacité à mobiliser et à mettre en relation des connaissances pertinentes, sa disposition à être en prise avec les enjeux contemporains, son esprit critique et sa capacité à développer une réflextion personnelle.
