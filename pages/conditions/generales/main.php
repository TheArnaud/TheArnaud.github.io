     		<h2>Préparation Sésame/Accès</h2>
        <p style="color:#000066; text-align:left;">
              <b>Les conditions générales sont spécifiques à chaque stage et formation. 
              <br><br>Les conditions ci-dessous sont données à titre d’exemple.
                            
              </b></p>
		<h4>Article 1.</h4>
		<p>
			Toute demande d’inscription doit être formulée par écrit, à l’aide de la fiche d’inscription ci-jointe qui tient lieu de contrat entre Optimum et le participant.
		</p>

		<h4>Article 2.</h4>
		<p>
			Les participants doivent être respectueux des enseignants, personnels administratifs, des locaux et du matériel mis à leur disposition.
		</p>

		<h4>Article 3.</h4>
		<p>
			Les participants aux stages doivent souscrire toute assurance nécessaire afin d’être couverts durant leur présence dans les locaux mis à leur disposition pour la formation. Optimum ne pourrait être tenu pour responsable de toute perte, dommage survenant directement ou indirectement durant le stage.
		</p>

		<h4>Article 4.</h4>
		<p>
			Toute admission à une formation est soumise à un règlement préalable de la part du participant. Toute formation achetée est due dans son intégralité. Le paiement sera effectué à l’inscription, comptant, sans escompte par chèque ou par virement.
		</p>

		<h4>Article 5.</h4>
		<p>
			Toute inscription annulée quinze jours avant le début du stage sera intégralement remboursée. Seuls les frais de dossier seront définitivement acquis à Optimum. Passé le délai de quinze jours, il est expressément convenu que l’intégralité des sommes versées à Optimum sera définitivement due, ne pouvant faire l’objet d’aucune demande de remboursement de la part du participant, et ce pour quel que motif que ce soit.
		</p>

		<h4>Article 6.</h4>
		<p>
			Toute annulation doit faire l’objet d’une notification écrite, datée et signée et adressée à Optimum par lettre recommandée avec avis de réception (AR).
		</p>

		<h4>Article 7.</h4>
		<p>
			L’inscription devient définitive dès confirmation de la part d’Optimum et envoi de la convocation au stage. Optimum se réserve le droit de refuser une inscription.
		</p>

		<h4>Article 8.</h4>
		<p>
			Le remboursement des sommes versées (déduction faite des frais de dossier) dégage Optimum de toute responsabilité.
		</p>
